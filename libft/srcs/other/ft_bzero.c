#include <strings.h>

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*str;

	i = 0;
	str = s;
	while (n > 0)
	{
		str[i] = '\0';
		i++;
		n--;
	}
	s = str;
}
