#include <unistd.h>

void	ft_print_tables(char **tab)
{
	int n;
	int m;

	n = 0;
	m = 0;
	while (tab[m] != 0)
	{
		n = 0;
		while (tab[m][n])
			write(1, &tab[m][n++], 1);
		write(1, "\n", 1);
		m++;
	}
}
