#include <string.h>

void	*ft_memset(void *b, int c, size_t len)
{
	int		i;
	char	*str;

	i = 0;
	str = b;
	while (len > 0)
	{
		str[i] = c;
		i++;
		len--;
	}
	b = str;
	return (b);
}
